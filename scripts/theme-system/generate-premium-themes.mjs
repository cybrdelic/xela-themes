/**
 * Generate surface gradient from base color
 */
function generateSurfaceGradient(base, type) {
  // For dark themes, surfaces get lighter
  // For light themes, surfaces get darker
  const surfaces = [];
  const steps = 8;

  for (let i = 0; i < steps; i++) {
    if (type === 'dark') {
      surfaces.push(adjustBrightness(base, i * 5));
    } else {
      surfaces.push(adjustBrightness(base, -i * 3));
    }
  }

  return surfaces;
}

/**
 * Adjust brightness of hex color
 */
function adjustBrightness(hex, amount) {
  // Remove # if present
  hex = hex.replace('#', '');

  // Convert to RGB
  const r = parseInt(hex.substring(0, 2), 16);
  const g = parseInt(hex.substring(2, 4), 16);
  const b = parseInt(hex.substring(4, 6), 16);

  // Adjust
  const newR = Math.max(0, Math.min(255, r + amount));
  const newG = Math.max(0, Math.min(255, g + amount));
  const newB = Math.max(0, Math.min(255, b + amount));

  // Convert back to hex
  return '#' +
    newR.toString(16).padStart(2, '0').toUpperCase() +
    newG.toString(16).padStart(2, '0').toUpperCase() +
    newB.toString(16).padStart(2, '0').toUpperCase();
}

