#!/usr/bin/env node
import fs from 'fs';
import path from 'path';
import { buildBrowserThemeData } from './browser-theme-data.mjs';

const root = path.resolve('.');
const outDir = path.join(root, 'exports', 'browser-themes');
const packageJson = JSON.parse(fs.readFileSync(path.join(root, 'package.json'), 'utf8'));
const index = buildBrowserThemeData();

function hexToRgb(hex, fallback = '#000000') {
  const raw = typeof hex === 'string' ? hex.trim() : '';
  const clean = (raw || fallback).replace(/^#/, '').slice(0, 6);
  const normalized = clean.length === 3
    ? clean.split('').map((ch) => ch + ch).join('')
    : clean.padEnd(6, '0');
  const value = Number.parseInt(normalized, 16);
  return [
    (value >> 16) & 255,
    (value >> 8) & 255,
    value & 255
  ];
}

function buildThemeManifest(theme) {
  const p = theme.palette;
  return {
    manifest_version: 3,
    name: `${theme.name} Browser Theme`,
    version: packageJson.version,
    description: `Browser chrome theme for ${theme.name}. Generated by XELA Themes.`,
    theme: {
      colors: {
        frame: hexToRgb(p.bg),
        frame_inactive: hexToRgb(p.bgAlt),
        toolbar: hexToRgb(p.bgElevated),
        toolbar_text: hexToRgb(p.text),
        tab_text: hexToRgb(p.text),
        tab_background_text: hexToRgb(p.textMuted),
        bookmark_text: hexToRgb(p.text),
        button_background: hexToRgb(p.buttonBg),
        toolbar_button_icon: hexToRgb(p.textSoft),
        omnibox_background: hexToRgb(p.inputBg),
        omnibox_text: hexToRgb(p.inputFg),
        omnibox_border: hexToRgb(p.inputBorder),
        ntp_background: hexToRgb(p.bg),
        ntp_text: hexToRgb(p.text),
        ntp_link: hexToRgb(p.link)
      }
    }
  };
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

ensureDir(outDir);

const manifests = [];
for (const theme of index.themes) {
  const themeDir = path.join(outDir, theme.id);
  ensureDir(themeDir);
  const manifest = buildThemeManifest(theme);
  fs.writeFileSync(
    path.join(themeDir, 'manifest.json'),
    JSON.stringify(manifest, null, 2) + '\n',
    'utf8'
  );
  manifests.push({
    id: theme.id,
    name: theme.name,
    type: theme.type,
    packIds: theme.packIds,
    packLabels: theme.packLabels,
    path: path.relative(root, path.join(themeDir, 'manifest.json')).replace(/\\/g, '/'),
    palette: theme.palette
  });
}

const readme = [
  'XELA Browser Themes',
  '',
  'Each subdirectory is a standalone Chrome/Edge browser theme package.',
  'Load one directory at a time via the browser extensions page:',
  '',
  '1. Open edge://extensions or chrome://extensions',
  '2. Enable Developer mode',
  '3. Click "Load unpacked"',
  '4. Pick exports/browser-themes/<theme-id>',
  '',
  'These browser themes are intentionally separate from the XELA site-theming extension.',
  'Chrome treats browser themes as a different package type.',
  ''
].join('\n');

fs.writeFileSync(path.join(outDir, 'README.txt'), readme, 'utf8');
fs.writeFileSync(
  path.join(outDir, 'index.json'),
  JSON.stringify({ generatedAt: new Date().toISOString(), packs: index.packs, themes: manifests }, null, 2) + '\n',
  'utf8'
);

console.log(`Browser theme packages: ${manifests.length} → ${outDir}`);
console.log('Load one subdirectory at a time as an unpacked browser theme.');
